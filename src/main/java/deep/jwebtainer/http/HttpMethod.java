package deep.jwebtainer.http;

/**
 * Represents the HTTP methods that can be used in a request.
 * This enum defines the standard HTTP methods as specified in the HTTP/1.1
 * protocol.
 */
public enum HttpMethod {
    /**
     * The GET method is used to retrieve data from a specified resource.
     */
    GET,
    /**
     * The POST method is used to submit data to be processed to a specified
     * resource.
     */
    POST;
}
